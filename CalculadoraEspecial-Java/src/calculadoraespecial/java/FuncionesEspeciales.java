package calculadoraespecial.java;

public class FuncionesEspeciales {
    public double valorAbsoluto (double a) {
        return Math.abs(a);
    }
    public double raiz (double a) {
        return Math.sqrt(a);
    }
    public double tangente (double a) {
        return Math.tan(a);
    }
    public double seno (double a) {
        return Math.sin(a);
    }
    
}
